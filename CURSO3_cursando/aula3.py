"""
Python = Linguagem de programação 
Tipo de tipagem = Dinâmica / Forte
str => string => texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Denis')
print(1, 'Denis "Barbosa"') # Denis "Barbosa"

# Aspas duplas
print("Denis")
print(2, "Denis 'Barbosa'") # Denis 'Barbosa'


# Escape
print('Denis \'Barbosa\'') # Denis 'Barbosa'

# r
print(r'Denis \'Barbosa\'') # Denis \'Barbosa\'
